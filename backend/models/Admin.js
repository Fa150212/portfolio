const mongoose = require("mongoose");

const adminSchema = new mongoose.Schema({
  firstName: {
    type: String,
    required: true,
  },
  lastName: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
  },
  password: {
    type: String,
    required: true,
  },
  photo: {
    type: String,
    default: "https://i.pravatar.cc/150",
  },
  role: {
    type: String,
    default: "Administrateur",
  },
});

module.exports = mongoose.model("Admin", adminSchema);

